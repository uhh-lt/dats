import json
import logging
import re
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

import mlflow
import pandas as pd

from schemas.config_schema import RunConfig

logger = logging.getLogger(__name__)


def _slugify(value: str) -> str:
    return re.sub(r"[^a-zA-Z0-9_-]+", "_", value).strip("_")


def _flatten_dict(
    data: dict[str, Any], prefix: str = ""
) -> dict[str, str | float | int | bool]:
    flattened: dict[str, str | float | int | bool] = {}

    for key, value in data.items():
        full_key = f"{prefix}.{key}" if prefix else str(key)
        if isinstance(value, dict):
            flattened.update(_flatten_dict(value, prefix=full_key))
            continue

        if isinstance(value, (str, int, float, bool)):
            flattened[full_key] = value
            continue

        flattened[full_key] = json.dumps(value, sort_keys=True)

    return flattened


def _write_local_artifacts(
    output_dir: Path,
    timestamp: str,
    run_name: str,
    metrics: dict[str, float],
    config: dict[str, Any],
    results_df: pd.DataFrame,
) -> tuple[Path, Path, Path]:
    slug_run_name = _slugify(run_name)
    csv_path = output_dir / f"{slug_run_name}_{timestamp}_details.csv"
    metrics_path = output_dir / f"{slug_run_name}_{timestamp}_metrics.json"
    config_path = output_dir / f"{slug_run_name}_{timestamp}_config.json"

    results_df.to_csv(csv_path, index=False)
    metrics_path.write_text(
        json.dumps(metrics, indent=2, sort_keys=True), encoding="utf-8"
    )
    config_path.write_text(
        json.dumps(config, indent=2, sort_keys=True), encoding="utf-8"
    )

    return csv_path, metrics_path, config_path


def _artifact_suffix_from_generated_name(artifact_path: Path) -> str:
    name = artifact_path.name
    if "_" not in name:
        return name
    return name.split("_", 1)[1]


def _rename_additional_artifacts_for_run(
    artifact_paths: list[Path],
    *,
    run_name: str,
    timestamp: str,
) -> list[Path]:
    slug_run_name = _slugify(run_name)
    renamed_paths: list[Path] = []

    for artifact_path in artifact_paths:
        if not artifact_path.exists():
            continue

        suffix = _artifact_suffix_from_generated_name(artifact_path)
        target_path = artifact_path.with_name(f"{slug_run_name}_{timestamp}_{suffix}")

        # Avoid overwriting if the target already exists from another artifact.
        collision_index = 1
        while target_path.exists() and target_path != artifact_path:
            target_path = artifact_path.with_name(
                f"{slug_run_name}_{timestamp}_{collision_index}_{suffix}"
            )
            collision_index += 1

        if target_path != artifact_path:
            artifact_path = artifact_path.rename(target_path)

        renamed_paths.append(artifact_path)

    return renamed_paths


def log_experiment_to_mlflow(
    run_config: RunConfig,
    metrics: dict[str, float],
    results_df: pd.DataFrame,
    additional_artifact_paths: list[Path] | None = None,
) -> dict[str, Any]:
    config = run_config.model_dump(mode="json")
    output_dir: Path = run_config.output_dir
    experiment_name = run_config.experiment.experiment_name
    configured_run_name = run_config.experiment.run_name
    mlflow_uri = run_config.mlflow_uri

    logger.info("Writing local benchmark artifacts to %s", output_dir)

    timestamp = datetime.now(timezone.utc).strftime("%Y%m%d_%H%M%S")
    tracking_info: dict[str, Any] = {
        "local_details_csv": None,
        "local_metrics_json": None,
        "local_config_json": None,
        "local_additional_artifacts": [],
        "effective_run_name": None,
        "mlflow_logged": False,
        "mlflow_run_id": None,
    }
    artifact_paths = additional_artifact_paths or []

    try:
        logger.info(
            "Logging run '%s' to MLflow experiment '%s' at %s",
            configured_run_name or "<auto-generated>",
            experiment_name,
            mlflow_uri,
        )
        mlflow.set_tracking_uri(str(mlflow_uri))
        mlflow.set_experiment(experiment_name)

        flat_params = _flatten_dict(config)
        metric_values = {k: float(v) for k, v in metrics.items()}

        run_context = (
            mlflow.start_run(run_name=configured_run_name)
            if configured_run_name
            else mlflow.start_run()
        )

        with run_context as run:
            # If run_name is omitted, MLflow auto-generates one. Reuse that name for local files.
            effective_run_name = (
                run.data.tags.get("mlflow.runName")
                or configured_run_name
                or run.info.run_id
            )
            csv_path, metrics_path, config_path = _write_local_artifacts(
                output_dir=output_dir,
                timestamp=timestamp,
                run_name=effective_run_name,
                metrics=metrics,
                config=config,
                results_df=results_df,
            )

            artifact_paths = _rename_additional_artifacts_for_run(
                artifact_paths,
                run_name=effective_run_name,
                timestamp=timestamp,
            )

            mlflow.log_params(flat_params)
            mlflow.log_metrics(metric_values)
            mlflow.log_artifact(str(csv_path))
            mlflow.log_artifact(str(metrics_path))
            mlflow.log_artifact(str(config_path))
            for artifact_path in artifact_paths:
                if artifact_path.exists():
                    mlflow.log_artifact(str(artifact_path))

            tracking_info["local_details_csv"] = str(csv_path)
            tracking_info["local_metrics_json"] = str(metrics_path)
            tracking_info["local_config_json"] = str(config_path)
            tracking_info["local_additional_artifacts"] = [
                str(path) for path in artifact_paths if path.exists()
            ]
            tracking_info["effective_run_name"] = effective_run_name
            tracking_info["mlflow_logged"] = True
            tracking_info["mlflow_run_id"] = run.info.run_id
            logger.info(
                "MLflow run logged successfully: run_id=%s, run_name=%s",
                run.info.run_id,
                effective_run_name,
            )
    except Exception as exc:
        # Keep local artifacts even if MLflow fails.
        effective_run_name = configured_run_name or "local_run"
        csv_path, metrics_path, config_path = _write_local_artifacts(
            output_dir=output_dir,
            timestamp=timestamp,
            run_name=effective_run_name,
            metrics=metrics,
            config=config,
            results_df=results_df,
        )
        artifact_paths = _rename_additional_artifacts_for_run(
            artifact_paths,
            run_name=effective_run_name,
            timestamp=timestamp,
        )
        tracking_info["local_details_csv"] = str(csv_path)
        tracking_info["local_metrics_json"] = str(metrics_path)
        tracking_info["local_config_json"] = str(config_path)
        tracking_info["local_additional_artifacts"] = [
            str(path) for path in artifact_paths if path.exists()
        ]
        tracking_info["effective_run_name"] = effective_run_name
        tracking_info["mlflow_error"] = str(exc)
        logger.exception("MLflow logging failed: %s", exc)

    return tracking_info
