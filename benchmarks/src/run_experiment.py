import json
import logging
from pathlib import Path

import hydra
from dotenv import load_dotenv
from omegaconf import DictConfig, OmegaConf

from core.runner import run_experiment
from schemas.config_schema import RunConfig

logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s %(levelname)s %(name)s: %(message)s",
)
logging.getLogger("httpx").setLevel(logging.WARNING)
logging.getLogger("httpcore").setLevel(logging.WARNING)


@hydra.main(version_base=None, config_path="../configs", config_name="config")
def main(cfg: DictConfig) -> None:
    # load env vars from .env file
    load_dotenv(dotenv_path=Path(__file__).parent / ".env")

    # load configs
    raw_config = OmegaConf.to_container(cfg, resolve=True)
    run_config = RunConfig.model_validate(raw_config)

    print(f"MLflow experiment: {run_config.experiment.experiment_name}")
    print(
        f"MLflow run: {run_config.experiment.run_name or '<auto-generated by MLflow>'}"
    )
    print(f"Using model: {run_config.experiment.model.name}")

    summary = run_experiment(run_config)
    print(json.dumps(summary, indent=2, sort_keys=True))


if __name__ == "__main__":
    main()
